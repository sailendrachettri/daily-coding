// Function to find square root
// x: element to find square root
class Solution {
  public:
    int floorSqrt(int n) {
        long long res = 1;
        
        for(int i = 1; i <= n; i++){
            if(i*i <= n){
                res = i;
            } else
                break;
        }
        
        return res;
    }
};

